    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        // TODO add your handling code here:
        try {
            Veiculo veiculo = new Veiculo();
            
            int capacidade = Integer.parseInt(this.campoCapacidade.getText());
            int ano = Integer.parseInt(this.campoAno.getText());
            
            veiculo.setPlaca(this.campoPlaca.getText());
            veiculo.setCapacidade(capacidade);
            veiculo.setAno(ano);
            veiculo.setModelo(this.campoModelo.getText());
            veiculo.setCor(this.campoCor.getText());
            veiculo.setTipo(this.campoTipo.getText());
            
            
            if (controlador == 1){
                veiculo.alterarVeiculo(veiculo);
                JOptionPane.showMessageDialog(this, "Ve√≠culo atualizado com sucesso!");
                dispose();
            } else {
                veiculo.cadastrarVeiculo(veiculo);
                JOptionPane.showMessageDialog(this, "Ve√≠culo cadastrado com sucesso!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadastrarCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CadastrarVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        dispose();
    }//GEN-LAST:event_SalvarActionPerformed

