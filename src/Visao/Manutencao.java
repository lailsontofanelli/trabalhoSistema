    public void atualizaLista(String plac){
        String[] nomesColunas = {"Código", "Veiculo", "Valor", "Data", "Descrição"};
        String mensagem = "";
        if (plac.length() > 0){
            lista = new ArrayList(ManutencaoControle.consultarManutPlaca(plac));
            mensagem = "Não existe manutenção do veículo " + plac + " cadastrado."; 
        }else{
            lista = new ArrayList(ManutencaoControle.consultarManutencao());
            mensagem = "Não existem Manutenções cadastrados.";
        }
        Object[][] dadosVetor = new Object[lista.size()][nomesColunas.length];
        for (int i=0; i<lista.size(); i++){
            ManutencaoControle m = (ManutencaoControle)lista.get(i);
            dadosVetor[i][0] = m.getId();
            dadosVetor[i][1] = m.getPlacaVeiculo();
            dadosVetor[i][2] = m.getValor();
            dadosVetor[i][3] = m.getData();
            dadosVetor[i][4] = m.getDescricao();
        }
        DefaultTableModel modelo = new DefaultTableModel(dadosVetor,nomesColunas);
        tabela.setModel(modelo);
        if(lista.isEmpty()){
            JOptionPane.showMessageDialog(this, mensagem);
        }
    }
