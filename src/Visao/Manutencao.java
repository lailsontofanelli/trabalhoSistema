    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        String placa = bPlaca.getText();
        if(placa.length() == 8){
            String p0 = placa.substring(0, 4);
            p0 = p0.toLowerCase();
            String p1 = placa.substring(4, 8);
            placa = p0 + p1;
        }
        if (calculaPlaca(placa) == false && placa.length() > 0){
            JOptionPane.showMessageDialog(this, "Placa não válida. Informe outra.\nEx.: AAA-0000.");
            bPlaca.setText("");
        }else{
            bPlaca.setText("");
            //System.out.println(placa);
            if (placa.length() < 1){
                JOptionPane.showMessageDialog(this, "Informe uma placa.");
                atualizaLista("");
            }
            else{
                atualizaLista(placa);
            }
        }
    }//GEN-LAST:event_buscarActionPerformed

