    private void calcularCombustivelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularCombustivelActionPerformed
        // TODO add your handling code here:
        double precoDisel = 2.50;
        double kilo;
        double custoCombus;
        double totalLitros;
        double totalKilo = Double.parseDouble(this.campoDistancia.getText());
        String campoLitro, campoCombustivel;

        //Cálculo do combustível referente a viagem
        String placa1 = jTextField1.getText();
        
        ArrayList lista1;
        lista1 = new ArrayList(Veiculo.consultarVeiculoPlaca(placa1));
        Veiculo veiculo = (Veiculo)lista1.get(0);
        
        kilo = veiculo.getKilometragem();
        totalLitros = (totalKilo / kilo) * 2;
        custoCombus = (totalLitros * precoDisel) * 2;

        campoLitro = String.valueOf(totalLitros);
        litros.setText(campoLitro);
        campoCombustivel = String.valueOf(custoCombus);
        custoCombustivel.setText(campoCombustivel);
    }//GEN-LAST:event_calcularCombustivelActionPerformed

