    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            String aux1, aux2;
            aux1 = campoDataPart.getText();
            aux2 = campoDataVolta.getText();
            if (aux1.length() > 0 && aux2.length() > 0){
                if (jTable1.getRowCount() != 0){
                    sel = new SelVeiculo(aux1,aux2,jTable1.getRowCount());
                    sel.setLocationRelativeTo(null);
                    sel.setVisible(true);
                    sel.setResizable(false);
                } else {
                    JOptionPane.showMessageDialog(this, "É necessário que todos os passageiros estejam adicionados para poder escolher o veículo.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Preencha o campo 'Data Partida' e 'Data volta' para continuar.");
            }
            sel.enviarTela(this);
        } catch (SQLException ex) {
            Logger.getLogger(ServicoViagem.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Digite uma entrada válida de data. (dd/mm/aaaa)");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ServicoViagem.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Digite uma entrada válida de data. (dd/mm/aaaa)");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

